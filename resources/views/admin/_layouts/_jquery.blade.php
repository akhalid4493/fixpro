<script>
function load_unseen_notification()
{
	$.ajax({
  	url:"{{ url(route('activities.index')) }}",
      type: "GET",
      success:function(res){
	      $('.dropdown-notification').html(res.view);
      },
  });
}

function load_alert_notification()
{
  $.ajax({
    url:"{{ url(route('activities.index')) }}",
      type: "GET",
      success:function(res){
        if(res.unseen_notification > 0)
        {
          playSound();
          swal({
            title: "طلب معاينة",
            text: "لديك طلب معاينة جديد",
            icon: "success",
						buttons: true,
						dangerMode: true,
          })
					.then((willDone) => {
						if (willDone) {
							updateSeen();
						}else{
							updateSeenWithoutRedirect();
						}
					});
        }
      },
  });
}

function playSound(){
  var audio = new Audio('{{ url('uploads/media/doorbell-5.mp3') }}');
  audio.play();
}

function updateSeenWithoutRedirect()
{
	$.ajax({
  	url:"{{ url(route('activities.update')) }}",
      type: "GET",
      success:function(res){
      },
  });
}

function updateSeen()
{
	$.ajax({
  	url:"{{ url(route('activities.update')) }}",
      type: "GET",
      success:function(res){
        window.location.href = "{{ url(route('previews.index')) }}";
      },
  });
}

load_unseen_notification();

setInterval(function(){
  load_alert_notification();
  load_unseen_notification();
}, 5000);

$(document).on('click', '.notifications', function(e) {
  updateSeen();
});

</script>

